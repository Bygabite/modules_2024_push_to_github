# Dictionaries- collections used to store key value pairs

emails = { 'Anne Stahl': 'astahl@gmail.com', 'Peter Small': 'peters@yandex.com', 'Mark Steel': 'mark@steel.com'}

print(emails['Mark Steel'])

# in list and tuples you access the individual element by providing index in square brackets
# in dictionaries access specific values by providing the key within same square brackets

spanish_animals = {'dog': 'el perro', 'cat': 'el gato', 'horse': 'el caballo', 'bird': 'el pajro'} 
# 'key': 'value'
print(spanish_animals['bird'])

